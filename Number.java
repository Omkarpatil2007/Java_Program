public class Number
{
	public static void main(String args[])
	{
		int a=18;
		int b=50;
		if (a>17 && b>30)
		{
			System.out.println("condition is true ");
		}
		
		if (a<15 || b<25)
		{
			System.out.println("condition is false");
		}
		
		if (!(a>20 && b>30))
		{
			System.out.println("condition is false");
		}
	}
}	